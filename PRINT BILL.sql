CREATE OR REPLACE PROCEDURE PRINT_BILL(P_ORDER_ID NUMBER)
AS
TYPE TP IS TABLE OF VARCHAR2(50);
V TP:=TP(NULL,NULL,NULL,NULL);
BEGIN
   SELECT COSTOMER_NAME,ORDER_ID,DATES
   INTO 
   V(1),V(2),V(3)
   FROM CUSTOMER_ALL_DEATELS WHERE ROWNUM<=1 AND ORDER_ID=P_ORDER_ID;
   DBMS_OUTPUT.PUT_LINE('CUSTOMER NAME :-'||V(1));
   DBMS_OUTPUT.PUT_LINE('ORDER ID :-'||V(2));
   DBMS_OUTPUT.PUT_LINE('DATE :-'||V(3));
   DBMS_OUTPUT.PUT_LINE('TOTAL COST :-'||V(4));
   DBMS_OUTPUT.PUT_LINE(' ');
   DBMS_OUTPUT.PUT_LINE('DEATELS');
   DBMS_OUTPUT.PUT_LINE('___________________');
END;

EXEC PRINT_BILL(3);