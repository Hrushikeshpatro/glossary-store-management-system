CREATE OR REPLACE PROCEDURE PRINT_BILL(P_ORDER_ID NUMBER)
AS
    DET SYS_REFCURSOR;
    V CUSTOMER_ALL_DEATELS%ROWTYPE;
BEGIN
    OPEN DET FOR SELECT C1.* FROM CUSTOMER_ALL_DEATELS C1 ORDER BY ORDER_ID;
    LOOP
        FETCH DET INTO V;
        EXIT WHEN DET%NOTFOUND;

        IF V.ORDER_ID = P_ORDER_ID THEN
            DBMS_OUTPUT.PUT_LINE(V.COSTOMER_NAME);
        ELSE    
            DBMS_OUTPUT.PUT_LINE(' ');
        END IF;
    END LOOP; 
END;
/
SET SERVEROUTP ON;
EXEC PRINT_BILL(3);
